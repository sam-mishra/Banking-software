    private double power(double base, double power) {
    double result = 1;

    if (power>0){
      while(power-->0) {
      result = result * base;
    }
    }
    else{
    for(;power<0;power++){
        result = result / base;
    }
    }       
    return result;
    }
    private double sphereVol(double radius){
        //return (4*radius*cylinderVol(radius,1))/3;
        return ((4*3.14*power(radius,3))/3);
    }
    private double pyramidVol(double side, double height){
       // return (power(side,2)*height*sphereVol(1))/12.56;
       return ((power(side,2)*height)/3);
    }
    private double cylinderVol(double radius, double height){
       // return (9.42*pyramidVol(1,height)*power(radius,2));
       return (3.14*power(radius,2)*height);
    }
