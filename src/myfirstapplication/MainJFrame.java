    private void jDivideButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDivideButtonActionPerformed
        // TODO add your handling code here:
        //checking input
        String fInput=jfInputTextField.getText();
        String sInput=jsInputTextField.getText();
        if(checkInput(fInput, sInput)){
            //reading data from fields
            double input1=Double.parseDouble(jfInputTextField.getText());
            double input2=Double.parseDouble(jsInputTextField.getText());
            //performing calcultaions
            if(input2==0){
                jResultLabel.setText("Cannot divide by zero"); //case when result is infinity
            }
            else{
                double output=input1/input2;
                //displaying the result in the output panel
                displayOutput(output);
            }
        }
    }//GEN-LAST:event_jDivideButtonActionPerformed

