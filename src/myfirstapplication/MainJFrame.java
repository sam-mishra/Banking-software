    private void jLoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jLoginButtonActionPerformed
        // TODO add your handling code here:
        if(theUser.isUser(jNameTextFieldLogin.getText(), jPasswordFieldLogin.getText(), (String)jLoginComboBox.getSelectedItem())){
            jStatusMessageLabel.setText(jNameTextFieldLogin.getText()+" is logged in...");
            jMainTabbedPane.addTab("Head Office Details",jTabInMemory2);
            jMainTabbedPane.addTab("Calculator",jTabInMemory3);
            if(theUser.isManager(jNameTextFieldLogin.getText(), jPasswordFieldLogin.getText(), jLoginComboBox.getSelectedItem().toString())||theUser.isAdvisor(jNameTextFieldLogin.getText(), jPasswordFieldLogin.getText(), jLoginComboBox.getSelectedItem().toString())||theUser.isAdministrator(jNameTextFieldLogin.getText(), jPasswordFieldLogin.getText(), jLoginComboBox.getSelectedItem().toString())){
                jMainTabbedPane.addTab("Edit Head Office Details",jTabInMemory4);
                if(theUser.isAdvisor(jNameTextFieldLogin.getText(), jPasswordFieldLogin.getText(), jLoginComboBox.getSelectedItem().toString())){
                    jSaveAddressButton.setEnabled(false);
                }
                if(theUser.isAdministrator(jNameTextFieldLogin.getText(), jPasswordFieldLogin.getText(), jLoginComboBox.getSelectedItem().toString())){
                    jSaveAddressButton.setEnabled(false);
                    jSaveOtherDetailsButton.setEnabled(false);
                }
            }
            jMainTabbedPane.setSelectedIndex(2);
        }
        else{
            jStatusMessageLabel.setText("Cannot login, please check name and password");
        }
    }//GEN-LAST:event_jLoginButtonActionPerformed

